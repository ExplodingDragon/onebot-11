// Populate the sidebar
//
// This is a script, and not included directly in the page, to control the total size of the book.
// The TOC contains an entry for each page, so if each page includes a copy of the TOC,
// the total size of the page becomes O(n**2).
class MDBookSidebarScrollbox extends HTMLElement {
    constructor() {
        super();
    }
    connectedCallback() {
        this.innerHTML = '<ol class="chapter"><li class="chapter-item expanded "><a href="index.html"><strong aria-hidden="true">1.</strong> 关于</a></li><li class="chapter-item expanded affix "><li class="spacer"></li><li class="chapter-item expanded affix "><li class="part-title">通信</li><li class="chapter-item expanded "><a href="communication/index.html"><strong aria-hidden="true">2.</strong> 通信概述</a></li><li class="chapter-item expanded "><a href="communication/http.html"><strong aria-hidden="true">3.</strong> HTTP</a></li><li class="chapter-item expanded "><a href="communication/http-post.html"><strong aria-hidden="true">4.</strong> HTTP POST</a></li><li class="chapter-item expanded "><a href="communication/ws.html"><strong aria-hidden="true">5.</strong> 正向 WebSocket</a></li><li class="chapter-item expanded "><a href="communication/ws-reverse.html"><strong aria-hidden="true">6.</strong> 反向 WebSocket</a></li><li class="chapter-item expanded "><a href="communication/authorization.html"><strong aria-hidden="true">7.</strong> 鉴权</a></li><li class="chapter-item expanded affix "><li class="spacer"></li><li class="chapter-item expanded affix "><li class="part-title">消息</li><li class="chapter-item expanded "><a href="message/index.html"><strong aria-hidden="true">8.</strong> 消息概述</a></li><li class="chapter-item expanded "><a href="message/string.html"><strong aria-hidden="true">9.</strong> 字符串格式</a></li><li class="chapter-item expanded "><a href="message/array.html"><strong aria-hidden="true">10.</strong> 数组格式</a></li><li class="chapter-item expanded "><a href="message/segment.html"><strong aria-hidden="true">11.</strong> 消息段类型</a></li><li class="chapter-item expanded affix "><li class="spacer"></li><li class="chapter-item expanded affix "><li class="part-title">API</li><li class="chapter-item expanded "><a href="api/index.html"><strong aria-hidden="true">12.</strong> API 概述</a></li><li class="chapter-item expanded "><a href="api/public.html"><strong aria-hidden="true">13.</strong> 公开 API</a></li><li class="chapter-item expanded "><a href="api/hidden.html"><strong aria-hidden="true">14.</strong> 隐藏 API</a></li><li class="chapter-item expanded affix "><li class="spacer"></li><li class="chapter-item expanded affix "><li class="part-title">事件</li><li class="chapter-item expanded "><a href="event/index.html"><strong aria-hidden="true">15.</strong> 事件概述</a></li><li class="chapter-item expanded "><a href="event/message.html"><strong aria-hidden="true">16.</strong> 消息事件</a></li><li class="chapter-item expanded "><a href="event/notice.html"><strong aria-hidden="true">17.</strong> 通知事件</a></li><li class="chapter-item expanded "><a href="event/request.html"><strong aria-hidden="true">18.</strong> 请求事件</a></li><li class="chapter-item expanded "><a href="event/meta.html"><strong aria-hidden="true">19.</strong> 元事件</a></li></ol>';
        // Set the current, active page, and reveal it if it's hidden
        let current_page = document.location.href.toString().split("#")[0];
        if (current_page.endsWith("/")) {
            current_page += "index.html";
        }
        var links = Array.prototype.slice.call(this.querySelectorAll("a"));
        var l = links.length;
        for (var i = 0; i < l; ++i) {
            var link = links[i];
            var href = link.getAttribute("href");
            if (href && !href.startsWith("#") && !/^(?:[a-z+]+:)?\/\//.test(href)) {
                link.href = path_to_root + href;
            }
            // The "index" page is supposed to alias the first chapter in the book.
            if (link.href === current_page || (i === 0 && path_to_root === "" && current_page.endsWith("/index.html"))) {
                link.classList.add("active");
                var parent = link.parentElement;
                if (parent && parent.classList.contains("chapter-item")) {
                    parent.classList.add("expanded");
                }
                while (parent) {
                    if (parent.tagName === "LI" && parent.previousElementSibling) {
                        if (parent.previousElementSibling.classList.contains("chapter-item")) {
                            parent.previousElementSibling.classList.add("expanded");
                        }
                    }
                    parent = parent.parentElement;
                }
            }
        }
        // Track and set sidebar scroll position
        this.addEventListener('click', function(e) {
            if (e.target.tagName === 'A') {
                sessionStorage.setItem('sidebar-scroll', this.scrollTop);
            }
        }, { passive: true });
        var sidebarScrollTop = sessionStorage.getItem('sidebar-scroll');
        sessionStorage.removeItem('sidebar-scroll');
        if (sidebarScrollTop) {
            // preserve sidebar scroll position when navigating via links within sidebar
            this.scrollTop = sidebarScrollTop;
        } else {
            // scroll sidebar to current active section when navigating via "next/previous chapter" buttons
            var activeSection = document.querySelector('#sidebar .active');
            if (activeSection) {
                activeSection.scrollIntoView({ block: 'center' });
            }
        }
        // Toggle buttons
        var sidebarAnchorToggles = document.querySelectorAll('#sidebar a.toggle');
        function toggleSection(ev) {
            ev.currentTarget.parentElement.classList.toggle('expanded');
        }
        Array.from(sidebarAnchorToggles).forEach(function (el) {
            el.addEventListener('click', toggleSection);
        });
    }
}
window.customElements.define("mdbook-sidebar-scrollbox", MDBookSidebarScrollbox);
